export const AllCallingCodesSelectOptions = Object.keys(
  CallingCodesPerCountry
).map((key) => {
  return {
    label: `${key} (+${
      CallingCodesPerCountry[key as keyof typeof CallingCodesPerCountry]
    })`,
    value: key,
  };
});
export function countryToCallableCountry(country?: Country): CallableCountries {
  if (
    !country ||
    country === Country.NO_TYPE ||
    country === Country.UNIVERSAL ||
    country === Country.HM
  ) {
    return Country.US;
  }
  return country;
}
